import { ref } from 'vue';
import { defineStore } from 'pinia';

export const useConsoleTasksStore = defineStore('console-tasks', () => {
	const tasks = ref([
		{
			id: 'birthday',
			title: 'День народження',
			description:
				'Марічка для святкування свого дня народження хоче купити a кг цукерок за ціною х грн за 1 кг і b пляшок лимонаду за ціною у грн за 1 пляшку. Складіть проєкт для визначення вартості цієї покупки',
			hints: [
				'Спочатку обчисліть вартість цукерок: кількість кг * ціна за 1 кг',
				'Потім обчисліть вартість лимонаду: кількість пляшок * ціна за 1 пляшку',
				'Загальна вартість - це сума вартості цукерок і лимонаду',
			],
			inputData: [{ label: 'a = 2 кг цукерок' }, { label: 'x = 150 грн за 1 кг' }, { label: 'b = 3 пляшки лимонаду' }, { label: 'y = 25 грн за пляшку' }],
			solution: `# Введення даних
a = float(input('Введіть кількість кг цукерок: '))
x = float(input('Введіть ціну за 1 кг цукерок: '))
b = float(input('Введіть кількість пляшок лимонаду: '))
y = float(input('Введіть ціну за 1 пляшку лимонаду: '))

# Обчислення вартості
candy_cost = a * x
lemonade_cost = b * y
total_cost = candy_cost + lemonade_cost

# Виведення результату
print(f'Загальна вартість покупки: {total_cost} грн')`,
			expectedAnswer: 375,
			checkAnswer: (answer) => Math.abs(answer - 375) <= 0.01,
		},
		{
			id: 'flowerbed',
			title: 'Клумба',
			description: 'На 1 м² площі клумби, що має довжину a та ширину b, припадає 4 саджанці. Потрібно порахувати довжину огорожі та кількість саджанців, які треба закупити.',
			hints: [
				'Довжина огорожі - це периметр прямокутника: 2 * (довжина + ширина)',
				'Площа клумби - це довжина * ширина',
				'Кількість саджанців - це площа клумби * 4 (оскільки на 1 м² припадає 4 саджанці)',
			],
			inputData: [{ label: 'a = 3 м (довжина)' }, { label: 'b = 2 м (ширина)' }],
			solution: `# Введення даних
a = float(input('Введіть довжину клумби (м): '))
b = float(input('Введіть ширину клумби (м): '))

# Обчислення довжини огорожі (периметра)
fence_length = 2 * (a + b)

# Обчислення площі клумби
area = a * b

# Обчислення кількості саджанців
plants_needed = area * 4

# Виведення результатів
print(f'Довжина огорожі: {fence_length} м')
print(f'Потрібно саджанців: {plants_needed} шт')`,
			expectedAnswers: {
				fence: 10,
				plants: 24,
			},
			checkAnswers: (fence, plants) => ({
				fence: Math.abs(fence - 10) <= 0.01,
				plants: Math.abs(plants - 24) <= 0.01,
			}),
		},
	]);

	return {
		tasks,
	};
});
